package dev.buijs.klutter.core.tasks.adapter.flutter


import dev.buijs.klutter.core.*
import dev.buijs.klutter.core.KlutterPrinter
import org.gradle.api.logging.Logging
import java.io.File

/**
 * @author Gillian Buijs
 */
internal class AndroidRootBuildGradleGenerator(
    private val android: Android,
): KlutterFileGenerator() {

    override fun generate() = writer().write()

    override fun printer() = AndroidRootBuildGradlePrinter()

    override fun writer() = AndroidRootBuildGradleWriter(android.file.resolve("build.gradle"), printer().print())

}

/**
 * @author Gillian Buijs
 */
internal class AndroidRootBuildGradlePrinter: KlutterPrinter {

    override fun print(): String {
        val dollar = "$"
        return """    
            |// Autogenerated by Klutter
            |// Do not edit directly
            |// See: https://buijs.dev/klutter
            |buildscript {
            |
            |   def klutterGradleFile = new File("$dollar{projectDir}/../klutter.gradle")
            |   if (!klutterGradleFile.exists()) {
            |       throw new GradleException("File not found ${dollar}klutterGradleFile")
            |   }
            |
            |   apply from: "${dollar}klutterGradleFile"
            |
            |   repositories {
            |       google()
            |       mavenCentral()
            |       maven { url = uri("https://repsy.io/mvn/buijs-dev/klutter") }
            |   }
            |
            |
            |   dependencies {
            |       classpath "com.android.tools.build:gradle:$dollar{project.ext["androidGradleVersion"]}"
            |       classpath "org.jetbrains.kotlin:kotlin-gradle-plugin:$dollar{project.ext["kotlinVersion"]}"
            |       classpath "dev.buijs.klutter:core:$dollar{project.ext["klutterVersion"]}"
            |   }
            |    
            |}
            |
            |allprojects {
            |    repositories {
            |        google()
            |        mavenCentral()
            |        maven { url = uri("https://repsy.io/mvn/buijs-dev/klutter") }
            |    }
            |}
            |
            |rootProject.buildDir = '../build'
            |subprojects {
            |    project.buildDir = "$dollar{rootProject.buildDir}/$dollar{project.name}"
            |}
            |
            |subprojects {
            |    project.evaluationDependsOn(':app')
            |}
            |
            |task clean(type: Delete) {
            |    delete rootProject.buildDir
            |}
    """.trimMargin()
    }

}


/**
 * @author Gillian Buijs
 */
internal class AndroidRootBuildGradleWriter(val file: File, val content: String): KlutterWriter {

    private val log = Logging.getLogger(AndroidRootBuildGradleWriter::class.java)

    override fun write() {

        if(file.exists()) {
            file.delete().also {
                log.debug("Deleted build.gradle: $file")
            }
        } else log.error("Build.gradle file in android folder not found. Creating a new file!")

        file.createNewFile().also { log.debug("Created new build.gradle: $file") }
        file.writeText(content).also { log.debug("Written content to $file:\r\n$content") }
    }

}